'''
Formatacao Basica de Strings

s - string
d - int
f - float
. <numero de digitos>f
x ou X - Hexadecimal
(Caractere)(><^)(quantidade)
> - esquerda
< - direita
^ - centro
Sinal + - ou -
Conversion Flags - !r !s !a

'''
variavel = 'ABCD'
print(f'{variavel}')
print(f'{variavel}: >10')
print(f'{variavel}: <10')
print(f'{variavel}: ^10')
print(f'{variavel}: ')
